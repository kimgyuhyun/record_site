-- 전체 스키마 초기화(스쿼시 베이스라인).
-- 과거엔 베이스 테이블을 ddl-auto 로 만들고 그 위에 증분 마이그레이션만 관리했는데, 베이스 테이블 CREATE 가
-- 마이그레이션에 없어서 "빈 DB"(신규 배포/CI)에서는 Flyway 가 부팅하지 못했다. 그래서 현재 엔티티 기준 전체
-- 스키마(Hibernate 가 MySQL 에 실제 생성한 DDL 을 mysqldump 로 그대로 떠온 것)를 단일 마이그레이션으로 합친다.
-- ddl-auto: validate 와 정확히 일치하므로 빈 DB 에서 이 파일만으로 부팅된다. 이후 변경은 V{timestamp} 로 누적한다.
--
-- FK 의존성 순서로 배치한다(참조되는 테이블 먼저). 마지막의 검색용 인덱스 3개는 엔티티 매핑엔 없고
-- 과거 add_search_indexes 마이그레이션이 추가하던 것이라, 성능 유지를 위해 여기에 함께 만든다.

